public enum WebSite {
    PEERTOPARK("www.peertopark.com"), GOOGLE("www.google.com");

    private final String value;

    private WebSite(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return getValue();
    }
    
}
